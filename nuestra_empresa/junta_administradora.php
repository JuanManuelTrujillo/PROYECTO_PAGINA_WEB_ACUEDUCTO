              <h1>Nuestra junta administradora</h1>
           <p>Una Junta Administradora Local (JAL) es parte de la estructura del Estado colombiano, dentro de la rama ejecutiva (con funciones normativas y de control político) en el orden territorial más bajo (corregimientos, comunas y localidades). Las JAL fueron consagradas por la Ley 136 de 1994, que las define como «corporaciones administrativas de carácter público, de elección popular».1​ Las JAL son corporaciones de Control político en Colombia.
      
