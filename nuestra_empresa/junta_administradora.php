          En Colombia, los municipios, están conformados por una cabecera municipal (como área urbana) y veredas (en el área rural). A su vez, el área urbana se pueden dividir en comunas y en el área rural en corregimientos, mientras que en los distritos especiales la división política administrativa se denominan localidades. Las localidades, comunas y corregimientos son administrados por las Juntas Administradoras Locales (JAL), y sus miembros deben ser elegidos por votación popular.
          
          En el año 1991, la Constitución Colombiana consagró la institución y la dispuso en su artículo 318 (2).2​
          
          Funciones
          Con el fin de mejorar la prestación de los servicios y asegurar la participación de la ciudadanía en el manejo de los asuntos públicos de carácter local, los concejos municipales podrán dividir sus municipios en comunas (cuando se trate de áreas urbanas) y en corregimientos (en el caso de las zonas rurales). En cada una de las comunas o corregimientos habrá una junta administradora local de elección popular, integrada por el número de miembros que determine la ley, que tendrá las siguientes funciones:
          
          Participar en la elaboración de los planes y programas municipales de desarrollo económico y social y de obras públicas.
          Vigilar y controlar la prestación de los servicios municipales en su comuna o corregimiento y las inversiones que se realicen con recursos públicos.
          Formular propuestas de inversión ante las autoridades nacionales, departamentales y municipales encargadas de la elaboración de los respectivos planes de inversión.
          Distribuir las partidas globales que les asigne el presupuesto municipal.
          Ejercer las funciones que les deleguen el concejo y otras autoridades locales. Las asambleas departamentales podrán organizar juntas administradoras para el cumplimiento de las funciones que les señale el acto de su creación en el territorio que este mismo determine</p>
      
